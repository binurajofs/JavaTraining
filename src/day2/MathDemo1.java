package day2;

public class MathDemo1 extends MathDemo {
	// create methods to do sub, mul and define it.
	public int sub(int num_a, int num_b) {
		return (num_a - num_b);
	}

	public int add(int num_a, int num_b) {
		return (num_a + num_b);
	}

	// define add method
	public int mul(int num_a, int num_b) {
		return (num_a * num_b);
	}

	public int div(int num_a, int num_b) {
		return (num_a / num_b);
	}
}
