package com.ofs.java.mels.day1;

public class Pattern5 {

	public static void main(String args[]) {
		
		for(int i=0;i<5;i++) {
			System.out.print("\n");	
			for(int k=0;k<=i;k++) {
			
			System.out.print("* ");	
				
			}
			for(int j=4;j>i;j--) {
				
				System.out.print("  ");	
				
			}
			for(int j=4;j>i;j--) {
				
				System.out.print("  ");	
				
			}

			for(int l=0;l<=i;l++) {
				
				System.out.print(" *");	
				
			}

			}
			
		}
		
	}

