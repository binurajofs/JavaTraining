package com.ofs.java.day2raj;

public class MethodDemo {
		// Create a method to add two number, and return the result(use return type)
		
		public void  addition(int num1,int num2) {
			int result=num1+num2;
			System.out.println("the sum OF 2 number is:"+result);
		}
		
		public static int addition(int num1,int num2,int num3) {
			int result=num1+num2+num3;
			return result;
		}
}
