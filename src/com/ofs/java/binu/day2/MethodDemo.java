public class MethodDemo {

	public void add(int num1, int num2) {
		System.out.println("Sum is " + (num1 + num2));
	}

	// method to add two number, and return the result(with return type)
	public int addReturns(int num1, int num2) {
		return (num1 + num2);
	}
}