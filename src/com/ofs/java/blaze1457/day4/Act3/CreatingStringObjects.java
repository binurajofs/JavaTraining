
public class CreatingStringObjects {
    
    public static void main(String args[]){
        // There are two ways of creating string object
        
        String str1 = "Hello " ;    // One way is by using literals
        String str2 = new String("Friends!"); // Second way is by using new keyword
    
        System.out.print(str1);
        System.out.print(str2);
    }

}
