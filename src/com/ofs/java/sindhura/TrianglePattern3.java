package com.ofs.java.sindhura;
import java.util.Scanner;
public class TrianglePattern3 {

	public static void main(String[] args)
	{
		
				int i=4;
				do 
				{
						int j=0;
						do 
						{
							System.out.print("* ");
							j++;
						}while(j<=i);
					
						System.out.println("");
						i--;
				}while(i>=0);
					    

	}

}
