package com.ofs.java.hamsa.day4.memorymanagement;

public class ForWhile {
	public static void main(String args[])
	{
	//for loop
	int input = 5;
	for(int i = 1; i <= input; i++ )
	{
	    System.out.println(i);
	}
	 
	//while loop
	int i = 1;
	while(i <= input)
	{
	    System.out.println(i);
	    i++;
	}
	}

}
